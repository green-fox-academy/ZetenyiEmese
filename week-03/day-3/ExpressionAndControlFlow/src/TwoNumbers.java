public class TwoNumbers {
    public static void main(String[] args) {
        System.out.println("Műveletek egész számokkal:\n");
        System.out.println("22+13=" + (22+13));
        System.out.println("22-13=" + (22-13));
        System.out.println("22*13=" + (22*13));
        System.out.println("22/13=" + (22./13.));
        System.out.println("22/13 egész része=" + (22/13));
        System.out.println("22/13 osztási maradéka=" + 22%13);
    }
}
